package com.company.FinalProject.Weather;

public class Wind {

    private double speed;
    private double degree;

    //getters + setters
    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }

    public double getDegree() {
        return degree;
    }

    public void setDegree(double degree) {
        this.degree = degree;
    }
}
